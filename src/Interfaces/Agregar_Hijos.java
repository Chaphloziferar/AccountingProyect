    private void EndActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EndActionPerformed
        try {
            if (Son_Name.getText().isEmpty() || Son_Born_Date.getText().isEmpty() || Son_Age.getText().isEmpty()) {
                throw new NullPointerException("Ha ocurrido un error");
            } else {
                son_name = Son_Name.getText();
                son_born_date = Son_Born_Date.getText();
                son_age = Integer.parseInt(Son_Age.getText());
            }

            hijo = new Hijo(son_name, son_born_date, son_age);
            Ingresar_Empleado.son_info.add(hijo);
            JOptionPane.showMessageDialog(null, "Datos guardados exitosamente", "Mensaje", JOptionPane.INFORMATION_MESSAGE);
            Son_Name.setText("");
            Son_Born_Date.setText("");
            Son_Age.setText("");
        } catch (NullPointerException | NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Ha ocurrido un error", "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_EndActionPerformed

